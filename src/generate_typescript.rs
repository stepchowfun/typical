    write_indentation(buffer, indentation + 3)?;
    writeln!(buffer, "default:")?;
    write_indentation(buffer, indentation + 4)?;
    writeln!(buffer, "offset += payloadSize;")?;
    write_indentation(buffer, indentation + 4)?;
